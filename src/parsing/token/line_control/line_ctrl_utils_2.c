#include "../../../../includes/minishell.h"

void	ft_ignore_quotes(char *l, int *i)
{
	while (l[*i] && (l[*i] == '\"' || l[*i] == '\''))
	{
		if (l[*i] == '\"')
		{
			(*i)++;
			while (l[*i] && l[*i] != '\"')
				(*i)++;
			if (!l[*i])
				(*i)--;
			if (!l[*i])
				break ;
		}
		else if (l[*i] == '\'')
		{
			(*i)++;
			while (l[*i] && l[*i] != '\'')
				(*i)++;
			if (!l[*i])
				(*i)--;
			if (!l[*i])
				break ;
		}
		else
			(*i)++;
	}
}
